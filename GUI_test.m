% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


ini_val = struct('orbit',[],'sp_param',[],'traf_fig',[],'terminal',[],...
    'optim_param',[],'opt_alg',[]);

rp = str2num(get(handles.edit1,'String'));
ra = str2num(get(handles.edit4,'String'));
inc = str2num(get(handles.edit5,'String'));
ini_val.orbit = [rp;ra;inc];

m0 = str2num(get(handles.edit6,'String'));
f1 = str2num(get(handles.edit7,'String'));
Isp1 = str2num(get(handles.edit8,'String'));
f2 = str2num(get(handles.edit10,'String'));
Isp2 = str2num(get(handles.edit11,'String'));
ini_val.sp_param = [m0;f1;Isp1;f2;Isp2]; 

if get(handles.radiobutton5,'Value')
    ecl = 1;
elseif get(handles.radiobutton6,'Value')
    ecl = 0;
end
if get(handles.checkbox1,'Value')   
    swt = str2num(get(handles.edit9,'String'));
else
    swt = 0;
end
ini_val.traf_fig = [ecl;swt];

tol_inc = str2num(get(handles.edit12,'String'));
tol_ecc = str2num(get(handles.edit13,'String'));
tol_a = str2num(get(handles.edit14,'String'));
max_rev = 3000;
ini_val.terminal = [tol_inc; tol_ecc; tol_a; max_rev];

slice = str2num(get(handles.edit15,'String'));
type = get(handles.listbox1,'Value');
ini_val.optim_param = [slice;type];

if get(handles.listbox1,'Value') == 1
    ini_val.opt_alg = optimoptions(@fminunc,'Display','off',...
    'Algorithm','quasi-newton','GradObj','on', ...
    'TolFun',1e-9);
elseif get(handles.listbox2,'Value') == 2
    ini_val.opt_alg = optimoptions(@fminunc,'Display','off',...
    'Algorithm','trust-region','GradObj','on','TolFun',1e-9);
end
main_optim(ini_val);
set(handles.text5,'String',(rp+ra)/2);



